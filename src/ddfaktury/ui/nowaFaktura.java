    @Action public void zapisz() throws ParseException {
        try{
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        Date sp = sdf.parse(jFormattedTextField2.getText());
        Date wyst = sdf.parse(jFormattedTextField1.getText());
            Faktury.add(Klienci.getByName(jComboBox1.getSelectedItem().toString()), Sprzedawca.get(), sp, wyst, jTextField1.getText(), przelicz(), platnosc.getSelectedItem().toString());
            for(int i=0; i<listaProduktow.getRowCount(); i++){                
                ListaProduktow.add(Produkty.getByName(listaProduktow.getValueAt(i, 0).toString()), Faktury.getByNumber(jTextField1.getText()) , Integer.valueOf(listaProduktow.getValueAt(i, 1).toString()), Double.valueOf(listaProduktow.getValueAt(i, 4).toString().replace(',', '.')));
            }

            ostrzezenie = true;
        }catch (NullPointerException npe){
            Error.println(npe);
            JOptionPane.showMessageDialog(this, "Brak jakiejkolwiek pozycji na fakturze!", "Błąd",0);
        }
            dispose();
    }

