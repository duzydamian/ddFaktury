    private double przelicz(){
        Double netto = 0.0, brutto = 0.0;
        for(int i=0; i<listaProduktow.getRowCount(); i++){
            netto += Double.valueOf(listaProduktow.getValueAt(i, 3).toString().replace(',', '.'))*Integer.valueOf(listaProduktow.getValueAt(i, 1).toString());
            brutto += Double.valueOf(listaProduktow.getValueAt(i, 4).toString().replace(',', '.'))*Integer.valueOf(listaProduktow.getValueAt(i, 1).toString());
        }
        jLabel6.setText(MyDouble.StringRet(netto));
        jLabel7.setText(MyDouble.StringRet(brutto-netto));
        jLabel8.setText(MyDouble.StringRet(brutto));
        return brutto;
    }
